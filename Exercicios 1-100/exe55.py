

lista = []

for c in range(1, 6):
    peso = float(input(f'Peso da {c}º pessoa: '))
    lista += [peso]
print(f'O maior peso foi: {max(lista)}Kg')
print(f'O menor peso foi: {min(lista)}Kg')

