pr = float(input("Primeira nota do aluno: "))
sg = float(input("Segunda nota do aluno: "))

print(f"A média entre {pr:.1f} e {sg:.1f} é {((sg+pr)/2):.1f}")

# O parâmetro :.1f determina quantos numeros terei depois da virgula no objeto em que foi posto. O número depois do f representa a quantidade de termos depois da virgula